// --------------------Simple if-else--------------------
let num1 = 10;
let num2 = 5;

let result;

if (num1 > num2) {
    result = num1 * 2;
}
else {
    result = num1 + num2;
}

console.log(result);


// --------------------Simple ternary operator--------------------
let number1 = 110;
let number2 = 50;

let results = (number1 > number2) ? number1 * 2 : number1 + number2;

console.log(results);
